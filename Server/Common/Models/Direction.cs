namespace Server.Common.Models;

/// <summary>
///     2D direction.
/// </summary>
public enum Direction
{
    /// <summary>
    ///     Up, towards negative Y values.
    /// </summary>
    North,

    /// <summary>
    ///     Down, towards positive Y values.
    /// </summary>
    South,

    /// <summary>
    ///     Left, towards negative X values.
    /// </summary>
    East,

    /// <summary>
    ///     Right, towards positive X values.
    /// </summary>
    West
}
